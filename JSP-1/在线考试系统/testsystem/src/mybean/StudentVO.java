package mybean;

public class StudentVO {
	private String number;
	private String studentname;
	private String studentpass;
	private String everychoice;
	private String everyscore;
	private int total;
	
	
	public String getNumber() {
		return number;
	}
	public void setNumber(String number) {
		this.number = number;
	}
	public String getStudentname() {
		return studentname;
	}
	public void setStudentname(String studentname) {
		this.studentname = studentname;
	}
	public String getStudentpass() {
		return studentpass;
	}
	public void setStudentpass(String studentpass) {
		this.studentpass = studentpass;
	}
	public String getEverychoice() {
		return everychoice;
	}
	public void setEverychoice(String everychoice) {
		this.everychoice = everychoice;
	}
	public String getEveryscore() {
		return everyscore;
	}
	public void setEveryscore(String everyscore) {
		this.everyscore = everyscore;
	}
	public int getTotal() {
		return total;
	}
	public void setTotal(int total) {
		this.total = total;
	}
	
	
	
}
